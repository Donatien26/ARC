package fr.insee.arc.ws.services.rest.generic.pojo;

public class GenericPojo {
	public String sandbox;
	
	public String targetPhase;

	public String fileName;
	public String version;
	public String validite;
	public String periodicite;
	public String query;
	public String fileContent;
	
	
	public String getSandbox() {
		return sandbox;
	}
	public void setSandbox(String sandbox) {
		this.sandbox = sandbox;
	}
	public String getTargetPhase() {
		return targetPhase;
	}
	public void setTargetPhase(String targetPhase) {
		this.targetPhase = targetPhase;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getValidite() {
		return validite;
	}
	public void setValidite(String validite) {
		this.validite = validite;
	}
	public String getPeriodicite() {
		return periodicite;
	}
	public void setPeriodicite(String periodicite) {
		this.periodicite = periodicite;
	}
	public String getQuery() {
		return query;
	}
	public void setQuery(String query) {
		this.query = query;
	}
	public String getFileContent() {
		return fileContent;
	}
	public void setFileContent(String fileContent) {
		this.fileContent = fileContent;
	}

	
	
	
}
